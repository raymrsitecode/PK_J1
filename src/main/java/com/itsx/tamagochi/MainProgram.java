    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        String seleccionado = (String)jComboBox1.getSelectedItem();

         System.out.println("com.itsx.tamagochi.MainProgram.jComboBox1ItemStateChanged()"+seleccionado);
        if(!seleccionado.equalsIgnoreCase("Seleccione") ){
                    System.out.println("com.itsx.tamagochi.MainProgram.jComboBox1ItemStateChanged()"+seleccionado);
                     Mascota mascoota = MascotaDao.getMascotaById(seleccionado); 
                     jLabel5.setText( "Nombre: "+mascoota.getName() );
                     Utils.selectActualMascotaImage(mascoota.getClase(),mascoota.getColor(), jLabel2);
                    jPanel1.setVisible(true);
                    jPanel2.setVisible(true);
                    
        } else{
                    jPanel1.setVisible(false);
                    jPanel2.setVisible(false);

} 
                   
    }//GEN-LAST:event_jComboBox1ItemStateChanged

